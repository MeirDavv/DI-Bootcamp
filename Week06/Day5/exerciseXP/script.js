const cardsList = document.getElementById("cardsList");
for (let robot of robots){
    const card = document.createElement("div");
    card.setAttribute("id","card");
    const cardImg = document.createElement("img");
    cardImg.setAttribute("src",robot.image);
    const cardName = document.createElement("h2");
    cardName.innerText = robot.name;
    const cardEmail = document.createElement("h4");
    cardEmail.innerText = robot.email;
    card.appendChild(cardImg);
    card.appendChild(cardName);
    card.appendChild(cardEmail);
    cardsList.appendChild(card);
}


const searchFilter = document.getElementById("filter");
searchFilter.addEventListener("keyup", searchRobots);

function searchRobots(){
    const input = searchFilter.value;
    
    for (let card of cardsList.children){
        cardTitle = card.getElementsByTagName('h2')[0].innerText;
        if(!cardTitle.toUpperCase().includes(input.toUpperCase()))
            card.style.display ="none";
        else
        card.style.display = "block";
    }
}
